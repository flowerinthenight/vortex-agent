//go:build linux

package main

import (
	"bytes"
	"encoding/binary"
	"errors"
	"fmt"
	"log/slog"
	"os"
	"os/signal"
	"strings"
	"syscall"

	"github.com/cilium/ebpf/link"
	"github.com/cilium/ebpf/ringbuf"
	"github.com/cilium/ebpf/rlimit"
)

//go:generate bpf2go -target amd64 -tags linux -cflags "-O2 -g -Wall -Werror" -type event bpf ./bpf/bpf.c -- -I./libbpf/src -I./vmlinux.h/include/x86_64

func main() {
	stopper := make(chan os.Signal, 1)
	signal.Notify(stopper, os.Interrupt, syscall.SIGTERM)

	// Allow the current process to lock memory for eBPF resources.
	if err := rlimit.RemoveMemlock(); err != nil {
		slog.Error("RemoveMemlock failed:", "err", err)
		return
	}

	// Load pre-compiled programs and maps into the kernel.
	objs := bpfObjects{}
	if err := loadBpfObjects(&objs, nil); err != nil {
		slog.Error("loadBpfObjects failed:", "err", err)
		return
	}

	defer objs.Close()
	slog.Info("BPF objects loaded successfully")

	sm, err := link.Kretprobe("sys_sendmsg", objs.SysSendmsgRet, nil)
	if err != nil {
		slog.Error("link.Kretprobe/sys_sendmsg failed:", "err", err)
		return
	}

	defer sm.Close()
	slog.Info("Kretprobe for sys_sendmsg attached successfully")

	rm, err := link.Kretprobe("sys_recvmsg", objs.SysRecvmsgRet, nil)
	if err != nil {
		slog.Error("link.Kretprobe/sys_recvmsg failed:", "err", err)
		return
	}

	defer rm.Close()
	slog.Info("Kretprobe for sys_recvmsg attached successfully")

	rd, err := ringbuf.NewReader(objs.Events)
	if err != nil {
		slog.Error("ringbuf reader failed:", "err", err)
		return
	}

	defer rd.Close()

	go func() {
		<-stopper

		if err := rd.Close(); err != nil {
			slog.Error("rd.Close failed:", "err", err)
			os.Exit(1)
		}
	}()

	var count uint64

	var line strings.Builder

	// bpfEvent is generated by bpf2go.
	var event bpfEvent
	for {
		record, err := rd.Read()
		if err != nil {
			if errors.Is(err, ringbuf.ErrClosed) {
				slog.Info("received signal, exiting..")
				return
			}

			slog.Error("reading from reader failed:", "err", err)
			continue
		}

		count++
		if count%1000 == 0 {
			slog.Info("count:", "v", count)
		}

		// Parse the ringbuf event entry into a bpfEvent structure.
		if err := binary.Read(bytes.NewBuffer(record.RawSample), binary.LittleEndian, &event); err != nil {
			slog.Error("parsing ringbuf event failed:", "err", err)
			continue
		}

		// slog.Info("record:", "comm", event.Comm, "direction", event.Direction, "bytes", event.Bytes)

		line.Reset()
		fmt.Fprintf(&line, "comm=%s, direction=%d, bytes=%d",
			event.Comm,
			event.Direction,
			event.Bytes,
		)

		slog.Info(line.String())
	}
}

